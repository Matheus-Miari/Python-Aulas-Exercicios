# Operadores Aritméticos em Python
a = 10
b = 3
# Adição
soma = a + b
print("Soma:", soma)  # 13
# Subtração
subtracao = a - b
print("Subtração:", subtracao)  # 7
# Multiplicação
multiplicacao = a * b
print("Multiplicação:", multiplicacao)  # 30
# Divisão com ponto flutuante
divisao = a / b
print("Divisão:", divisao)  # 3.333...
# Divisão inteira
divisao_inteira = a // b
print("Divisão inteira:", divisao_inteira)  # 3
# Módulo (resto da divisão)
modulo = a % b
print("Módulo:", modulo)  # 1
# Potência
potencia = a ** b
print("Potência:", potencia)  # 1000
