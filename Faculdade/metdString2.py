# Métodos de string em Python

# str.capitalize()       -> Retorna a string com o primeiro caractere em maiúsculo.
# str.casefold()         -> Retorna a string toda em minúsculo (mais agressivo que lower).
# str.center(width)      -> Centraliza a string, preenchendo com espaços.
# str.count(sub)         -> Conta quantas vezes a substring 'sub' aparece.
# str.encode()           -> Codifica a string para bytes.
# str.endswith(suffix)   -> Verifica se a string termina com 'suffix'.
# str.expandtabs(tabsize=8) -> Substitui tabulações '\t' por espaços.
# str.find(sub)          -> Retorna o índice da primeira ocorrência de 'sub', ou -1 se não encontrar.
# str.format()           -> Formata a string com placeholders.
# str.format_map(mapping)-> Formata a string com um dicionário.
# str.index(sub)         -> Igual ao find, mas levanta ValueError se não encontrar.
# str.isalnum()          -> Verifica se todos os caracteres são alfanuméricos.
# str.isalpha()          -> Verifica se todos os caracteres são letras.
# str.isascii()          -> Verifica se todos os caracteres são ASCII.
# str.isdecimal()        -> Verifica se todos os caracteres são decimais.
# str.isdigit()          -> Verifica se todos os caracteres são dígitos.
# str.isidentifier()     -> Verifica se a string é um identificador válido em Python.
# str.islower()          -> Verifica se todos os caracteres são minúsculos.
# str.isnumeric()        -> Verifica se todos os caracteres são numéricos.
# str.isprintable()      -> Verifica se todos os caracteres são imprimíveis.
# str.isspace()          -> Verifica se a string é composta apenas por espaços em branco.
# str.istitle()          -> Verifica se a string está no formato de título.
# str.isupper()          -> Verifica se todos os caracteres são maiúsculos.
# str.join(iterable)     -> Junta os elementos de um iterável usando a string como separador.
# str.ljust(width)       -> Alinha a string à esquerda, preenchendo com espaços.
# str.lower()            -> Converte todos os caracteres para minúsculo.
# str.lstrip()           -> Remove espaços em branco à esquerda.
# str.partition(sep)     -> Divide a string na primeira ocorrência de 'sep'.
# str.replace(old, new)  -> Substitui todas as ocorrências de 'old' por 'new'.
# str.rfind(sub)         -> Retorna o maior índice da ocorrência de 'sub', ou -1.
# str.rindex(sub)        -> Igual ao rfind, mas levanta ValueError se não encontrar.
# str.rjust(width)       -> Alinha a string à direita, preenchendo com espaços.
# str.rpartition(sep)    -> Divide a string na última ocorrência de 'sep'.
# str.rsplit(sep=None)   -> Divide a string da direita para a esquerda.
# str.rstrip()           -> Remove espaços em branco à direita.
# str.split(sep=None)    -> Divide a string em uma lista.
# str.splitlines()       -> Divide a string em uma lista, separando por quebras de linha.
# str.startswith(prefix) -> Verifica se a string começa com 'prefix'.
# str.strip()            -> Remove espaços em branco nas extremidades.
# str.swapcase()         -> Inverte maiúsculas e minúsculas.
# str.title()            -> Converte para formato de título.
# str.upper()            -> Converte todos os caracteres para maiúsculo.
# str.zfill(width)       -> Preenche a string com zeros à esquerda até atingir 'width'.
